const mongoose = require('mongoose');

const Contact_Schema = new mongoose.Schema({
    first_name: {
        type: String,
        required: true
    },
    last_name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: Number,
        required: true
    },
    message: {
        type: String,
        required: true
    },

});

const Contact = new mongoose.model('Customer Contact', Contact_Schema);

module.exports = Contact;